<h1>Presentación</h1>

<section>
  <h2>Qué es RePoMIt</h2>
  <p>
    RePoMIt, <em>Repertorio de la poesía en castellano en manuscritos italianos</em>,
    es un repertorio digital dedicado a composiciones poéticas en castellano conservadas
    en manuscritos italianos de los siglos XVI y XVII.
  </p>
</section>

<section>
  <h2>Origen del proyecto</h2>
  <p>
    El repertorio nace en el marco del seminario
    <em>Corre manuscrita (in Italia): Fonti, metodi e strumenti per lo studio della poesia aurea</em>,
    coordinado por Antonietta Molinaro en la Università eCampus, con la participación de
    Álvaro Cuéllar, Universitat Autònoma de Barcelona.
  </p>
</section>

<section>
  <h2>Objetivos</h2>
  <p>
    El objetivo principal es ofrecer una vía de acceso ordenada y verificable a los
    testimonios manuscritos italianos que transmiten poesía castellana áurea, con fichas
    de poemas, datos bibliográficos y herramientas de consulta.
  </p>
</section>

<section>
  <h2>Equipo y colaboraciones</h2>
  <p>
    La dirección científica corresponde a Antonietta Molinaro, Università eCampus.
    El desarrollo digital está a cargo de Álvaro Cuéllar, Universitat Autònoma de Barcelona.
    El proyecto cuenta con el patrocinio del Instituto Cervantes, AISPI, AISI y
    Università eCampus / DiSUS.
  </p>
</section>

<section>
  <h2>Cómo consultar el repertorio</h2>
  <p>
    El usuario puede consultar el repertorio alfabético, acceder a las fichas de manuscritos
    o utilizar la búsqueda por íncipit, verso, ítem, autor, forma, testimonio y texto libre.
    La búsqueda ignora puntuación, mayúsculas y tildes.
  </p>
</section>

<section>
  <h2>Cómo participar</h2>
  <p>
    La versión actual es preliminar. Las colaboraciones pueden orientarse a la identificación
    de nuevos testimonios, revisión de fichas, control de atribuciones, normalización métrica
    o mejora de datos bibliográficos.
  </p>
</section>

<section>
  <h2>Cómo señalar errores o sugerencias</h2>
  <p>
    Las correcciones y sugerencias deberán indicar, siempre que sea posible, el testimonio,
    el ítem afectado y la fuente que justifica la modificación.
  </p>
</section>

<section>
  <h2>Créditos</h2>
  <p>
    RePoMIt es una herramienta académica en desarrollo. Su contenido y su infraestructura
    digital se revisarán progresivamente a medida que se incorporen nuevos testimonios y
    criterios de catalogación compartidos.
  </p>
</section>
