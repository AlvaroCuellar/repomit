<h1>Criterios de catalogación</h1>

<section>
  <h2>Unidad de catalogación</h2>
  <p>
    RePoMIt trabaja siempre con testimonios manuscritos completos. La catalogación no parte
    de poemas aislados, sino de la secuencia de composiciones transmitida por cada manuscrito.
  </p>
</section>

<section>
  <h2>Testimonio y orden topográfico</h2>
  <p>
    Cada composición se sitúa según su orden topográfico dentro del manuscrito. Este criterio
    permite conservar la relación entre la ficha poética y la organización material del testimonio.
  </p>
</section>

<section>
  <h2>Ítem</h2>
  <p>
    El ítem se forma mediante la sigla del testimonio y el orden topográfico de la composición.
    Por ejemplo, una composición puede identificarse como testimonio + número de orden.
  </p>
</section>

<section>
  <h2>Criterios de transcripción y modernización gráfica</h2>
  <p>
    La versión preliminar distingue los campos descriptivos de la transcripción completa.
    Cuando se incorpore transcripción, se mantendrá la trazabilidad con el testimonio y se
    explicitarán los criterios de modernización gráfica aplicados.
  </p>
</section>

<section>
  <h2>Campos principales de la ficha</h2>
  <p>
    Las fichas recogen íncipit, segundo verso, éxplicit, epígrafe, atribución, forma métrica,
    esquema métrico, estructura interna, estribillo, transcripción y responsabilidad de la ficha.
  </p>
</section>

<section>
  <h2>Búsqueda normalizada</h2>
  <p>
    La búsqueda ignora puntuación, mayúsculas y tildes. Esto permite localizar variantes
    gráficas de un mismo término sin exigir coincidencia literal estricta.
  </p>
</section>

<section>
  <h2>Formas métricas y vocabulario controlado</h2>
  <p>
    Las formas métricas pertenecen a un vocabulario controlado. Este control favorece la
    consulta por forma y la comparación entre testimonios.
  </p>
</section>

<section>
  <h2>Tratamiento de barras / y |</h2>
  <p>
    Cuando procede, la barra oblicua se visualiza como salto de verso y la barra vertical
    como separación entre unidades o composiciones. El texto original importado se conserva
    en los datos.
  </p>
</section>

<section>
  <h2>Estado preliminar de los datos</h2>
  <p>
    Los datos se encuentran en fase preliminar. Las fichas, atribuciones, formas métricas y
    referencias bibliográficas podrán revisarse a medida que avance el proyecto.
  </p>
</section>
