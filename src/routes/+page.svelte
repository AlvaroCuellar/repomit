<h1>RePoMIt</h1>

<p>
  RePoMIt es una base inicial para un repertorio digital de poesía áurea conservada
  en manuscritos italianos.
</p>

<p>
  Esta fase solo prepara la aplicación, las rutas principales y una estructura
  local de datos de ejemplo.
</p>

